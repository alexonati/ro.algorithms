package ro.siit;

public class IncrementDecrement {
	public static void main(String[] args) {
		int i = 3;
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i);

		System.out.println(i);
		System.out.println(--i);
		System.out.println(i--);
		System.out.println(i);
	}
}